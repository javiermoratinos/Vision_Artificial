I=imread('estrella1.bmp');
A=[1 0 320;0 1 240;0 0 1];
B=[0.707 -0.707 0;0.707 0.707 0;0 0 1]; 
C=[1 0 -320;0 1 -240;0 0 1];
D=A*B*C;
